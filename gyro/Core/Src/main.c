float map(float x, float in_min, float in_neu, float in_max, float out_min, float out_neu, float out_max)
{
		if (x < in_neu)
		{
			return out_neu - (in_neu - x)*(out_neu - out_min)/(in_neu - in_min);
		}
	  else if (x > in_neu)
		{
			return out_neu + (x - in_neu)*(out_max - out_neu)/(in_max - in_neu);
		}
		else return out_neu;
}

float constrain(float x, float min, float max) 
{
    if (x < min) return min;
    else if (x > max) return max;
    else return x;
}

void HAL_TIM_PWM_PulseFinishedCallback(TIM_HandleTypeDef *htim)
{
		float gyro_rate = MPU6050_Read_Gyro(&hi2c1);
		pid_in = fir(gyro_rate);
		PID_Compute(&TPID);
		float tx_out_map = map((float)steer_ppm, STEER_PPM_MIN, STEER_PPM_NEU, STEER_PPM_MAX, SERVO_MIN_EPA, SERVO_NEUTRAL, SERVO_MAX_EPA);
	  float pid_out_map = map(pid_out, -2000, 0, 2000, SERVO_MIN_EPA - SERVO_NEUTRAL, 0, SERVO_MAX_EPA - SERVO_NEUTRAL);
		servo_ppm = constrain(pid_out_map + tx_out_map, SERVO_MIN_EPA, SERVO_MAX_EPA);	
		TIM14->CCR1 = (uint16_t)servo_ppm;
}

